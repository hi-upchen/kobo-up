#!/usr/bin/env node
/**
 * Builds the sanitized demo Kobo database shipped at `public/demo/KoboReader-demo.sqlite`.
 *
 * The real source file lives outside this repo, at
 * `../sample-kobo-folder/KoboReader.sqlite` (sibling to the project, the same
 * developer fixture used by `textGenerator.integration.test.ts`) — it is the
 * project owner's actual Kobo reading database, with his real book titles,
 * authors, highlights, and account email. This script never ships that data.
 * Instead it:
 *
 *   1. Picks a handful of real books from the source database (identified
 *      only by their opaque ContentID, which carries no personal meaning)
 *      purely to get a realistic reading-history shape (multiple books, a
 *      mix of highlight/note counts).
 *   2. Replaces every piece of human-readable content on those books —
 *      title, author, and all highlight/annotation text — with fictional
 *      public-domain book identities and real (public-domain, freely
 *      quotable) passages from those books.
 *   3. Deletes every other table's rows outright (Bookmark is entirely
 *      rebuilt from scratch; `user`, `Event`, `AnalyticsEvents`, etc. are
 *      wiped to zero rows) — none of those tables are read by the app, and
 *      several (`user`) contain the owner's real email address.
 *
 * Uses `sql.js` — the same WebAssembly SQLite library the production app
 * uses to parse Kobo databases in the browser — so the generated file is
 * guaranteed to be structurally identical to what the app already knows how
 * to read.
 *
 * Usage: `node scripts/build-demo-db.mjs`
 * Requires the source file to exist locally; skips (exit 0) otherwise, since
 * this script only needs to run when regenerating the shipped demo artifact,
 * not as part of every contributor's install or CI run.
 */
import fs from 'fs'
import path from 'path'
import { fileURLToPath } from 'url'
import initSqlJs from 'sql.js'

const __dirname = path.dirname(fileURLToPath(import.meta.url))

// Sibling folder to this repo — same convention as
// src/utils/__tests__/textGenerator.integration.test.ts
